    private void buscarbntActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarbntActionPerformed
      
        Connection con = null;
        
        try{
        
        con = conexion();
        ps = con.prepareStatement("SELECT * FROM edificios WHERE ID = ? ");
        ps.setString(1, rutbuscarTXT.getText());
        
        rs = ps.executeQuery();
        
        if(rs.next()){
        rutbuscarTXT.setText(rs.getString("ID"));
        IDtxt.setText(rs.getString("ID"));
        nombreTXT.setText(rs.getString("Nombre"));
        descripcion_TXT.setText(rs.getString("Descripcion"));
        rutTXT.setText(rs.getString("Rut_Admin")); 
        pisosTXT.setText(rs.getString("Npisos"));
        fechaTXT.setText(rs.getString("Fecha"));
//----------------------------------------------------------------------------------        
        IDtxt.setEnabled(false);
        rutTXT.setEnabled(false);
        fechaTXT.setEnabled(false);

        }else{
        JOptionPane.showMessageDialog(null, "no existe");
        }
        
        } catch (SQLException e) {
             System.err.println(e);
         }
        
        
    }//GEN-LAST:event_buscarbntActionPerformed

