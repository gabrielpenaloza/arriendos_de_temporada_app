    private void guardarbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarbtnActionPerformed

        Connection con = null;

        try{

            con = conexion();
            PreparedStatement ps = ps = con.prepareStatement ("UPDATE edificios SET Nombre =?, Descripcion =?, Npisos=? WHERE ID =? ");
            ps.setString(1, nombreTXT.getText());
            ps.setString(2, descripcion_TXT.getText());
            ps.setString(3, pisosTXT.getText());
            ps.setString(4, rutbuscarTXT.getText());
            
            int rs = ps.executeUpdate();

            if(rs >0 ){
                JOptionPane.showMessageDialog(null,"Edificio actualizado con exito");
            }
        }catch (Exception e){
            System.err.println(e);
            JOptionPane.showMessageDialog(null,"ha ocurrido un error");
        }
    }//GEN-LAST:event_guardarbtnActionPerformed

